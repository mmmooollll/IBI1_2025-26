a = 5.08  # 2004年苏格兰预估人口
b = 5.33  # 2014年苏格兰预估人口
c = 5.55  # 2024年苏格兰预估人口
d = b - a  # 2004-2014年人口变化
e = 5.55 - b  # 2014-2025年人口变化（实验文档写2025，数据源为2024的5.55百万）
# 对比d和e的大小，判断增长趋势
print("2004-2014年人口变化：", d)
print("2014-2025年人口变化：", e)
if e < d:
    print("# 苏格兰人口增长速度放缓（减速）")
elif e > d:
    print("# 苏格兰人口增长速度加快（加速）")
else:
    print("# 苏格兰人口增长速度不变")

